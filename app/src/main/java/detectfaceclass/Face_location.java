public class Face_location {
    private int height;

    private int left;

    private int top;

    private int width;

    public Face_location() {

    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHeight() {
        return this.height;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getLeft() {
        return this.left;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getTop() {
        return this.top;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getWidth() {
        return this.width;
    }

}